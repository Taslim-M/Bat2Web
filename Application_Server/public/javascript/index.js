function showMarkers() {
  clearAllMarkers();

  for (var incid of parsed_incidents) {
    console.log(incid.latitude);

    const marker = new google.maps.Marker({
      position: { lat: incid.latitude, lng: incid.longitude },
      map: map,
    });

    const contentString =
      '<div id="content">' +
      '<h3 id="firstHeading" class="firstHeading">' +
      incid.bat_species +
      "</h3>" +
      '<div id="bodyContent">' +
      "<b>Time: </b>" +
      incid.time +
      "</br><b>Latitude: </b>" +
      incid.latitude +
      "</br><b>Longitude: </b>" +
      incid.longitude +
      "</div>" +
      "</div>";

    const infowindow = new google.maps.InfoWindow({
      content: contentString,
    });
    marker.addListener("click", () => {
      infowindow.open(map, marker);
    });

    allMarkers.push(marker);
  }
}

function getHeatmapData() {
  /* Data points defined as an array of LatLng objects */
  var heatMapData = [];
  for (var incid of parsed_incidents) {
    heatMapData.push(new google.maps.LatLng(incid.latitude, incid.longitude));
  }

  return heatMapData;
}

function heatmapControl(controlDiv, map) {
  // Set CSS for the control border.
  const controlUI = document.createElement("div");
  controlUI.style.backgroundColor = "#fff";
  controlUI.style.border = "2px solid #000";
  controlUI.style.borderRadius = "3px";
  controlUI.style.boxShadow = "0 2px 6px rgba(0,0,0,.3)";
  controlUI.style.cursor = "pointer";
  controlUI.style.marginBottom = "22px";
  controlUI.style.textAlign = "center";
  controlUI.title = "Click to hide markers and view heatmap";
  controlDiv.appendChild(controlUI);
  // Set CSS for the control interior.
  const controlText = document.createElement("div");
  controlText.style.color = "rgb(25,25,25)";
  controlText.style.fontFamily = "Roboto,Arial,sans-serif";
  controlText.style.fontSize = "16px";
  controlText.style.lineHeight = "38px";
  controlText.style.paddingLeft = "5px";
  controlText.style.paddingRight = "5px";
  controlText.innerHTML = "Show Heatmap";
  controlUI.appendChild(controlText);
  // Setup the click event listeners: simply set the map to Chicago.
  var show = true;
  controlUI.addEventListener("click", () => {
    if (show) {
      show = false;
      clearAllMarkers();
      var heatmapData = getHeatmapData();
      heatmap = new google.maps.visualization.HeatmapLayer({
        data: heatmapData,
      });
      heatmap.setMap(map);
      controlUI.title = "Click to hide heatmap and view markers";
      controlText.innerHTML = "Hide Heatmap";
    } else {
      show = true;
      window.heatmap.setMap(null);
      showMarkers();
      controlUI.title = "Click to hide markers and view heatmap";
      controlText.innerHTML = "Show Heatmap";
    }
  });
}

// Sets the map on all markers in the array.
function setMapOnAll(map) {
  for (let i = 0; i < allMarkers.length; i++) {
    allMarkers[i].setMap(map);
  }
}

// Removes the markers from the map, but keeps them in the array.
function clearAllMarkers() {
  setMapOnAll(null);
}
